class Node:
    def __init__(self, info): 
        self.info = info  
        self.left = None  
        self.right = None 
        self.level = None 

    def __str__(self):
        return str(self.info) 

class BinarySearchTree:
    def __init__(self): 
        self.root = None

    def create(self, val):  
        if self.root == None:
            self.root = Node(val)
        else:
            current = self.root
         
            while True:
                if val < current.info:
                    if current.left:
                        current = current.left
                    else:
                        current.left = Node(val)
                        break
                elif val > current.info:
                    if current.right:
                        current = current.right
                    else:
                        current.right = Node(val)
                        break
                else:
                    break

"""
Node is defined as
self.left (the left child of the node)
self.right (the right child of the node)
self.info (the value of the node)
"""

# Function to print the Post Order Traversal of a Binary Tree
def postOrder(root):

    # If root is NULL then simply return
    if root == None:
        return
    
    
    postOrder(root.left)             # First Traverse Recursively Through left subtree.
    postOrder(root.right)            # Then Traverse Recursively to Through right subtree.
    print(root.info,end = " ")       # Then print the node value.

