vector<int> icecreamParlor(int m, vector<int> arr) {
    
    vector<int> result;
    
    // suppose current value of arr is taken
    // then second value must be (m - taken value) 
    for(int i = 0; i < arr.size(); i++){
        int flavour2 = m - arr[i];
        
        // then search (m - taken value) in arr
        // if found then we got the solution
        // push both taken value and (m - taken value)
        for(int j = i + 1; j < arr.size(); j++){
           
            if(arr[j] == flavour2){
                result.push_back(i + 1);
                result.push_back(j + 1);
                break;
            }
           
        }
        
    }

    return result;
}

