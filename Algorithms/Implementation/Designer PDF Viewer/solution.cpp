int designerPdfViewer(vector<int> h, string word) {
    
    int heighest = 0, area;
    
    // find index of each letter of word in vector h,
    // get h[index],
    // find heighest value of letter from h 
    for(int i = 0; i < word.size(); i++){
        int index = (int)(word[i]) - 97;
        if(heighest  < h[index])
            heighest = h[index];
    }
    
    // calculate area
    area = heighest * word.size();
    return area;
}

